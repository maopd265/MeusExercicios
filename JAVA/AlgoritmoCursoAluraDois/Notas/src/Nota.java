public class Nota {
    private String aluno;
    private double nota;

    public Nota(String nomeAluno,double notaAluno) {
        this.aluno=nomeAluno;
        this.nota=notaAluno;
        

    }

    
    public String getAluno() {
        return aluno;
    }

    public double getNota() {
        return nota;
    }

   

}
